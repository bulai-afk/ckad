async function ensureDevAuthorId(): Promise<number> {
  const requiredAuthorId = 1;
  const existing = await prisma.user.findUnique({ where: { id: requiredAuthorId }, select: { id: true } });
  if (existing) return existing.id;
  const created = await prisma.user.create({
    data: {
      email: "admin@example.com",
      password: "dev-password",
      name: "Admin",
      role: "ADMIN",
    },
    select: { id: true },
  });
  return created.id;
}

async function upsertArticle(a: SeedArticle, authorId: number) {
  const existing = await prisma.page.findUnique({
    where: { slug: a.slug },
    select: { id: true },
  });

  const blocks = [
    { type: "text", order: 0, data: { text: a.body } },
    { type: "summary", order: 1, data: { text: a.description } },
  ] as const;

  if (!existing) {
    await prisma.page.create({
      data: {
        title: a.title,
        slug: a.slug,
        status: "PUBLISHED",
        authorId,
        blocks: { create: blocks as any },
      },
    });
    return;
  }

  await prisma.$transaction([
    prisma.page.update({
      where: { id: existing.id },
      data: {
        title: a.title,
        slug: a.slug,
        status: "PUBLISHED",
        authorId,
      },
    }),
    prisma.block.deleteMany({ where: { pageId: existing.id } }),
    prisma.block.createMany({
      data: blocks.map((b) => ({ pageId: existing.id, type: b.type, order: b.order, data: b.data })) as any,
    }),
  ]);
}

async function main() {
  const authorId = await ensureDevAuthorId();
  for (const a of ARTICLES) {
    await upsertArticle(a, authorId);
  }
  // eslint-disable-next-line no-console
  console.log(`OK: seeded ${ARTICLES.length} article pages under articles/`);
}

main()
  .catch((e) => {
    // eslint-disable-next-line no-console
    console.error(e);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect().catch(() => {});
  });

